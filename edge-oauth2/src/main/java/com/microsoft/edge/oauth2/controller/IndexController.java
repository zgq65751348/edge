@RestController
@RequestMapping(value = "/index")
public class IndexController {

    @Autowired
    private UserService userService;

    @PostMapping(value = "/login")
    public CommonResult<UserDetails> login(HttpServletRequest request) throws IOException {
        String userInfo = GetRequestJsonUtils.getRequestPostStr(request);
        return CommonResult.success(userService.loadUserByUsername(userInfo)) ;
    }
}
