@Service
public class UserService implements UserDetailsService {
    @Autowired
    private PasswordEncoder passwordEncoder;

    @Autowired
    private UserMapper userMapper;

    @Autowired
    private Sub2AuthorityMapper sub2AuthorityMapper;

    @Autowired
    private UserAuthorityMapper userAuthorityMapper;

    @Override
    public UserDetails loadUserByUsername(String username) throws UsernameNotFoundException {
        UserExample example = new UserExample();
        long count = userMapper.countByExample(example);
        if (count == 0L) {
            User root = User.builder().accountExpired(true).accountLocked(true).phone(13522088684L).username("root").password(passwordEncoder.encode("123456"))
                    .credentialsExpired(true).enabled(true).createTime(new Date()).build();
            userMapper.insertSelective(root);

        }
        UserExample.Criteria criteria = example.createCriteria();
        criteria.andUsernameEqualTo(username);
        List<User> userList = userMapper.selectByExample(example);
        if(CollectionUtils.isEmpty(userList)){
            throw new UsernameNotFoundException("用户匹配失效");
        }
        SecurityUser securityUser = new SecurityUser();
        BeanUtils.copyProperties(userList.get(0),securityUser);
        UserAuthorityExample userAuthorityExample = new UserAuthorityExample();
        UserAuthorityExample.Criteria criteria1 = userAuthorityExample.createCriteria();
        criteria1.andUserIdEqualTo(userList.get(0).getId());
        List<UserAuthority> userAuthorities = userAuthorityMapper.selectByExample(userAuthorityExample);
        if(!CollectionUtils.isEmpty(userAuthorities)){
            List<Integer> authors = new LinkedList<>();
            userAuthorities.forEach(userAuthority -> {
                authors.add(userAuthority.getSub2AuthorityId());
            });
            Sub2AuthorityExample sub2AuthorityExample = new Sub2AuthorityExample();
            Sub2AuthorityExample.Criteria criteria2 = sub2AuthorityExample.createCriteria();
            criteria1.andSub2AuthorityIdIn(authors);
            List<Sub2Authority>sub2Authorities = sub2AuthorityMapper.selectByExample(sub2AuthorityExample);
            if(!CollectionUtils.isEmpty(sub2Authorities)){
                Collection<SecurityUser.Authority> authorities = new LinkedList<>();
                sub2Authorities.forEach(sub2Authority -> {
                    authorities.add(SecurityUser.Authority.builder().authority(sub2Authority.getAuthority()).build());
                });
                securityUser.setAuthorities(authorities);
            }
        }
       return securityUser;
    }
}
